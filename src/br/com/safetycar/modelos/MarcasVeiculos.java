package br.com.safetycar.modelos;

public class MarcasVeiculos {
	
	private long id;
	
	private String nomeMarca;

		
	public void setId(long id) {
		this.id = id;
	}

	public long getId() {
		return id;
	}

	public void setNomeMarca(String nomeMarca) {
		this.nomeMarca = nomeMarca;
	}

	public String getNomeMarca() {
		return nomeMarca;
	}

}
